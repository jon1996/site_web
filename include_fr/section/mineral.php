                                    <p>Tungsten, or wolfram, is a chemical element with the symbol W and atomic number 74. Tungsten is a rare metal found naturally on Earth almost exclusively combined with other elements in chemical compounds. It was identified as a new element in 1781 and first isolated as a metal in 1783. Its important ores include scheelite, and wolframite, lending the element its alternate name. The free element is remarkable for its robustness, especially the fact that it has the highest melting point of all the elements discovered, melting at 3,422 °C (6,192 °F; 3,695 K). It also has the highest boiling point, at 5,555 °C (10,031 °F; 5,828 K). Its density is 19.25 grams per cubic centimetre, comparable with that of uranium and gold, and much higher (about 1.7 times) than that of lead. Polycrystalline tungsten is an intrinsically brittle and hard material (under standard conditions, when uncombined), making it difficult to work. However, pure single-crystalline tungsten is more ductile and can be cut with a hard-steel hacksaw. Tungsten occurs in many alloys, which have numerous applications, including incandescent light bulb filaments, X-ray tubes, electrodes in gas tungsten arc welding, superalloys, and radiation shielding. Tungsten's hardness and high density make it suitable for military applications in penetrating projectiles. Tungsten compounds are often used as industrial catalysts. Tungsten is the only metal in the third transition series that is known to occur in biomolecules, being found in a few species of bacteria and archaea. However, tungsten interferes with molybdenum and copper metabolism and is somewhat toxic to most forms of animal life.</p>
